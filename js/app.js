//-----------Product quntity and Price calclutaion---------
document.getElementById('buynow-button1').addEventListener('click', function(){
    //---------Product quntity----------
    const preProductQuntity = document.getElementById('products-quantity');
    const preProductQuntityText = preProductQuntity.innerText;
    const preProductQuntityNum  = parseInt(preProductQuntityText );
    const finalProductQuntityNum = preProductQuntityNum +1 ;
    preProductQuntity.innerText = finalProductQuntityNum ;
    //---------All Product price----------
    const preAllProductPrice = document.getElementById('all-products-price');
    const preAllProductPriceText = preAllProductPrice.innerText;
    const preAllProductPriceNum = parseFloat(preAllProductPriceText);
    
    //----------Product Price---------
    const preProductPrice = document.getElementById('product-price-1');
    const preProductPriceText = preProductPrice.innerText;
    const preProductPriceNum = parseFloat(preProductPriceText);
    const finalProductPrice = preAllProductPriceNum + preProductPriceNum;
    preAllProductPrice.innerText = finalProductPrice;
    
    

    
})

